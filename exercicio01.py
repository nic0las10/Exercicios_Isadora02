'''Decisão entre Dois Números (Igualdade):
Crie um programa que recebe dois números inteiros e determina se eles são iguais ou diferentes. Imprima "São iguais" ou "São diferentes".'''

Num1 = int(input("Digite um numero inteiro:"))
num2 = int(input("Digite outro numero inteiro:"))

if Num1 == num2:
    print("Os numeros  são iguais!")
else:
    print("Os números são diferentes!")    
