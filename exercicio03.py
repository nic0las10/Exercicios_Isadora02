'''
Decisão Simples:
Peça ao usuário para digitar uma letra. Se a letra for 'a', imprima "Você digitou a letra 'a'." Caso contrário, imprima "Você digitou uma letra diferente de 'a'."
'''

letra_Usuário = input("Digite uma letra: ")

if letra_Usuário == "A":
    print("Voce digitou a letra A!")
else:
    print("Voce digitou uma letra diferente de A!")
